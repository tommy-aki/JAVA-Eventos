    private void crear_BUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crear_BUTTONActionPerformed
        // TODO add your handling code here:
        String user=nick_FIELD.getText();
        String nombre=nombre_FIELD.getText();
        String contra=passw_FIELD.getText();
        String edadStr = edad_FIELD.getText();
        int edad=Integer.parseInt(edadStr);
        int tipo=tipo_FIELD.getSelectedIndex();
        
        if(
                !user.isEmpty() &&
                !nombre.isEmpty() &&
                !contra.isEmpty() &&
                !edadStr.isEmpty() &&
                ((CuentaAdministrador)cuenta).addAcc(user, contra, nombre, edad, tipo)){
            nick_FIELD.setText("");
            nombre_FIELD.setText("");
            passw_FIELD.setText("");
            edad_FIELD.setText("");
            
            actualizarLista();
        }
        
    }//GEN-LAST:event_crear_BUTTONActionPerformed

    private void borrar_BUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrar_BUTTONActionPerformed
        // TODO add your handling code here:
        String nick=search_FIELD.getText();
        if(!cuenta.borrarAcc(nick)){
            JOptionPane.showMessageDialog(this, "Cuenta no encontrada");
        }
        actualizarLista();
        search_FIELD.setText("");
    }//GEN-LAST:event_borrar_BUTTONActionPerformed

    private void edit_BUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_BUTTONActionPerformed
        // TODO add your handling code here:
        String nick=search_FIELD.getText();
        if(Usuarios.buscar(nick)!=null){
            this.dispose();
            new Edit_FORM(cuenta, Usuarios.buscar(nick)).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Cuenta no encontrada");
        }
        search_FIELD.setText("");
    }//GEN-LAST:event_edit_BUTTONActionPerformed

