    private void checkPrivilege(Cuentas cuenta){
        if(cuenta.getTier()<2){
            crear_BUTTON.setEnabled(true);
            eliminar_BUTTON.setEnabled(true);
            editar_BUTTON.setEnabled(true);
        }
        if(cuenta.getTier()<1){
            admin_BUTTON.setEnabled(true);
        }
    }
    
